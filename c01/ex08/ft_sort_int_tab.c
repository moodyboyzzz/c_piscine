void	ft_sort_int_tab(int *tab, int size)
{
	int		i;
	int		z;

	i = 0;
	size--;
	while (i < size)
	{
		if (tab[i] > tab[i + 1])
		{
			z = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = z;
			i = -1;
		}
		i++;
	}
}
